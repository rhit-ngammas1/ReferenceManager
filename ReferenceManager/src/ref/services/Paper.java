package ref.services;

public class Paper {
	public String topic;
	public String paperid;
	public String papertitle;
	public String link;
	public Paper(String topic, String paperid, String papertitle, String link) {
		this.topic = topic;
		this.paperid = paperid;
		this.papertitle = papertitle;
		this.link = link;
	}
}
